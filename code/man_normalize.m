function [ M ] = man_normalize( M )
    M(187,:) = M(153,:);
    M(88,:) = M(182,:);
    %M(302,:) = M(105,:);
    %M(132,:) = M(251,:);

    %M(:,1:11) = (M(:,1:11).*0);
    %M(:,13) = (M(:,13).*128);
    %M(:,1) = ((M(:,1)-2)./6); % Width
    %M(:,2) = ((M(:,2)-32)./128); % ROB size
    %M(:,3) = ((M(:,3)-8)./72); % IQ size
    %M(:,4) = ((M(:,4)-8)./72); % LSQ size
    %M(:,5) = ((M(:,5)-40)./120); % RF sizes
    %M(:,6) = ((M(:,6)-2)./14); % RF read ports
    %M(:,7) = ((M(:,7)-1)./7); % RF write ports
    %M(:,8) = ((M(:,8)-1024)./31744); % Gshare
    %M(:,9) = ((M(:,9)-256)./768); % BTB size
    %M(:,10) = ((M(:,10)-8)./24); % Branches allowed
    %M(:,11) = ((M(:,11)-64)./960); % L1 Icache size
    %M(:,12) = ((M(:,12)-64)./960); % L1 Dcache size
    %M(:,13) = ((M(:,13)-512)./7680); % L2 Ucache size
    %M(:,14) = ((M(:,14)-9)./27); % Depth
end

