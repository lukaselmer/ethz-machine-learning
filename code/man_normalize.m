function [ M ] = man_normalize( M )
    M(:,1) = ((M(:,1)-2)./6)-0.5; % Width
    M(:,2) = ((M(:,2)-32)./128)-0.5; % ROB size
    M(:,3) = ((M(:,3)-8)./72)-0.5; % IQ size
    M(:,4) = ((M(:,4)-8)./72)-0.5; % LSQ size
    M(:,5) = ((M(:,5)-40)./120)-0.5; % RF sizes
    M(:,6) = ((M(:,6)-2)./14)-0.5; % RF read ports
    M(:,7) = ((M(:,7)-1)./7)-0.5; % RF write ports
    M(:,8) = ((M(:,8)-1024)./31744)-0.5; % Gshare
    M(:,9) = ((M(:,9)-256)./768)-0.5; % BTB size
    M(:,10) = ((M(:,10)-8)./24)-0.5; % Branches allowed
    M(:,11) = ((M(:,11)-64)./960)-0.5; % L1 Icache size
    M(:,12) = ((M(:,12)-64)./960)-0.5; % L1 Dcache size
    M(:,13) = ((M(:,13)-512)./7680); % L2 Ucache size
    M(:,14) = ((M(:,14)-9)./27)-0.5; % Depth
end

